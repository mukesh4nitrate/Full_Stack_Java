package DAY21;

public class Wrapper {
	public static void main(String[] args) {
		
		
		byte num1 = 127;
		Byte num2  = num1;		//autoboxing
		byte a  = num2;			//unboxing
		
	
		
		short num3 = 128;
		Short num4 = num3;		//autoboxing
		short b = num4;			//unboxing
		 
		
		 int num5 = 3344;
		 Integer num6 = num5;	//autoboxing
		 int c = num6;			//unboxing
		 
		 
		 long num7 = 9940274542l;
		 Long num8 = num7;			//autoboxing
		 long d = num8;				//unboxing
		 
		 
		 float num9 = 1.23f;
		 Float num10 = num9;		//autoboxing
		 float e = num10;			//unboxing
		 
		 
		 double num11 = 12.123d;
		 Double num12 = num11;		//autoboxing
		 double f = num12;			//unboxing
		 
		 
		char num13 = 'S';
		Character num14 = num13;		//autoboxing
		char g = num14;					//unboxing
		
		
		boolean num15 = true;
		Boolean num16 = num15;		//autoboxing
		boolean h = num16;			//unboxing
		 
		
		System.out.println(num8);
	}

}


